class map_t {
    // ==============================================================================
    // ==============================================================================
    // ==============================================================================
    class map_t_z {
    public:
        map_t_z () {
            _data = nullptr;
        }
        
        ~map_t_z () {
            _data = nullptr;
        }
        
        
        int set (int* map_array = nullptr) {
            _data = map_array;
            return 0;
        }
        
        
        int& operator [] (int i) {
            _data += i;
            return *_data;
        }
        //int& operator [] (int i);
        
        int* _data;
    };
    // =============================================================================
    // =============================================================================
    // =============================================================================
    class map_t_yz {
    public:
        map_t_yz ()
        {
            _data = nullptr;
        }
        
        ~map_t_yz () {
            _data = nullptr;
        }
        
        int set (int* map_array = nullptr) {
            _data = map_array;
            return 0;
        }
        
        int set_z (int z) {
            _z = z;
            return 0;
        }
        
        map_t_z& operator [] (int i) {
            _map_z.set (_data + i * _z);
            return _map_z;
        }
        
        
        //map_t_z& operator [] (int i);
        
        int* _data;
        map_t_z _map_z;
        int _z;
        
    };
    // =============================================================================
    // =============================================================================
    // =============================================================================
    
    map_t_yz _map_yz;
    
public:
    map_t (int map_x = 10, int map_y = 10, int map_z = 10, int* map_array = nullptr):
    _x (map_x),
    _y (map_y),
    _z (map_z),
    _data (map_array)
    {
        _map_yz.set_z(_z);
    };
    
    ~map_t () {
        _data = nullptr;
    };
    
    map_t_yz& operator [] (int i) {
        _map_yz.set (_data + i * _y * _z);
        return _map_yz;
    }
    
    
    int* _data;
    int _x;
    int _y;
    int _z;
};



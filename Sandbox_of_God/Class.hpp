enum structure_box_t {
    SKY         = 0,
    GRASS       = 1,
    EARTH       = 2,
    STONE       = 3
};

enum visibility_box_t {
    VISIBLE     = 1,
    NOTVISIBLE  = 0
};


class box_t {
public:
    box_t (int structure_box = 0, int visibility_box = 0):
    _structure(structure_box),
    _visibility(visibility_box)
    {}
    ~box_t () {
        _structure = 0;
        _visibility = 0;
    }
    int _structure;
    int _visibility;
};

